#include "main.h"

/**
 * mul - Multiplies two integers.
 * @a: The first integer to be multiplied.
 * @b: The second integer to be multiplied.
 *
 * Description:
 * This function takes two integers as input and returns their product. It
 * simply multiplies the values of @a and @b and returns the result.
 *
 * Parameters:
 * @a: The first integer to be multiplied.
 * @b: The second integer to be multiplied.
 *
 * Return:
 * The product of @a and @b.
 */

int mul(int a, int b)
{
	return (a * b);
}
